import pandas as pd

def evaluate_model(df: pd.DataFrame, classifier: AdvancedEmailTagClassifier) -> Dict:
    """Comprehensive evaluation with error analysis"""
    results = []
    errors = []

    print(f"📊 Evaluating on {len(df)} emails...")

    for idx, row in df.iterrows():
        prediction = classifier.classify(
            row['customer_id'],
            row['subject'],
            row['body']
        )

        # Use 'tag' column name (not 'actual_tag')
        actual_tag = row['tag']
        prediction['actual_tag'] = actual_tag
        prediction['correct'] = prediction['predicted_tag'] == actual_tag
        prediction['email_id'] = row['email_id']
        prediction['subject'] = row['subject']

        results.append(prediction)

        if not prediction['correct']:
            errors.append(prediction)

        # Progress indicator
        if (idx + 1) % 10 == 0:
            print(f"   Processed {idx + 1}/{len(df)} emails...")

    # Calculate metrics
    correct = sum(1 for r in results if r['correct'])
    total = len(results)
    accuracy = correct / total if total > 0 else 0

    # Per-customer accuracy
    customer_accuracy = {}
    for customer_id in df['customer_id'].unique():
        customer_results = [r for r in results if r['customer_id'] == customer_id]
        customer_correct = sum(1 for r in customer_results if r['correct'])
        customer_accuracy[customer_id] = customer_correct / len(customer_results) if len(customer_results) > 0 else 0

    return {
        'overall_accuracy': accuracy,
        'correct_predictions': correct,
        'total_predictions': total,
        'customer_accuracy': customer_accuracy,
        'all_results': results,
        'errors': errors
    }


def print_error_analysis(errors: List[Dict], max_errors: int = 5):
    """Detailed error analysis"""
    print("\n" + "="*80)
    print("ERROR ANALYSIS")
    print("="*80)

    if not errors:
        print("\n✅ Perfect! No classification errors.\n")
        return

    print(f"\n❌ Total Errors: {len(errors)}\n")
    print(f"Showing first {min(max_errors, len(errors))} errors:\n")

    for i, error in enumerate(errors[:max_errors], 1):
        print(f"\n{'─'*80}")
        print(f"Error #{i}:")
        print(f"{'─'*80}")
        print(f"Customer:  {error['customer_id']}")
        print(f"Email:     {error['subject']}")
        print(f"Predicted: {error['predicted_tag']} (confidence: {error['confidence']:.2f})")
        print(f"Actual:    {error['actual_tag']}")
        print(f"Reasoning: {error['reasoning']}")

        # Analyze why it failed
        if error['predicted_tag'] and error['actual_tag']:
            pred_words = set(error['predicted_tag'].split('_'))
            actual_words = set(error['actual_tag'].split('_'))
            overlap = pred_words & actual_words

            print(f"\n💡 Analysis:")
            if overlap:
                print(f"   → Partial match: {overlap} (close but not exact)")
                print(f"   → Need more specific pattern matching")
            else:
                print(f"   → Completely different categories")
                print(f"   → May need additional training examples")

