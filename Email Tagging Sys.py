    def classify(self, customer_id: str, subject: str, body: str) -> Dict:
        """
        Classify email using advanced pattern matching
        """
        text = subject + " " + body

        # Get valid tags for this customer
        valid_tags = self.isolation_system.customer_tags.get(customer_id, set())

        if not valid_tags:
            return {
                'customer_id': customer_id,
                'predicted_tag': None,
                'confidence': 0.0,
                'is_valid': False,
                'validation_msg': f"Unknown customer: {customer_id}",
                'reasoning': "No valid tags for customer"
            }

        # Calculate scores
        tag_scores = self.calculate_tag_scores(text, valid_tags)

        if tag_scores:
            # Get top prediction
            predicted_tag = max(tag_scores.items(), key=lambda x: x[1])[0]
            max_score = tag_scores[predicted_tag]

            # Calculate confidence based on score and separation from second best
            sorted_scores = sorted(tag_scores.values(), reverse=True)
            if len(sorted_scores) > 1:
                separation = sorted_scores[0] - sorted_scores[1]
                confidence = min(max_score / 15.0, 0.95)  # Cap at 0.95
                if separation > 5.0:
                    confidence = min(confidence + 0.1, 0.98)
            else:
                confidence = min(max_score / 15.0, 0.90)

            # Get top 3 candidates for reasoning
            top_candidates = sorted(tag_scores.items(), key=lambda x: x[1], reverse=True)[:3]
            reasoning = f"Top scores: " + ", ".join([f"{tag}({score:.1f})" for tag, score in top_candidates])
        else:
            # Fallback to most common tag for customer
            examples = self.isolation_system.customer_examples.get(customer_id, [])
            if examples:
                tag_counts = Counter([ex['tag'] for ex in examples])
                predicted_tag = max(tag_counts.items(), key=lambda x: x[1])[0]
                confidence = 0.4
                reasoning = f"Fallback to most common tag (appears {tag_counts[predicted_tag]} times)"
            else:
                predicted_tag = list(valid_tags)[0] if valid_tags else None
                confidence = 0.2
                reasoning = "Random fallback"

        # Validate
        is_valid, validation_msg = self.isolation_system.validate_prediction(
            customer_id, predicted_tag
        )

        return {
            'customer_id': customer_id,
            'predicted_tag': predicted_tag,
            'confidence': confidence,
            'is_valid': is_valid,
            'validation_msg': validation_msg,
            'reasoning': reasoning
        }

