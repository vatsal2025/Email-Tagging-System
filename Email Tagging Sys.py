class AdvancedEmailTagClassifier:
    """Enhanced LLM-based email classifier with sophisticated pattern matching"""

    def __init__(self, isolation_system: CustomerIsolatedTaggingSystem):
        self.isolation_system = isolation_system
        self.patterns = ENHANCED_PATTERNS

    def extract_advanced_features(self, text: str) -> Dict:
        """Extract comprehensive features from email"""
        text_lower = text.lower()

        features = {
            'primary_matches': [],
            'secondary_matches': [],
            'exact_phrases': [],
            'word_density': {},
            'length': len(text.split())
        }

        # Check for exact multi-word phrases
        exact_phrases = [
            'mail merge', 'shared mailbox', 'dark mode', 'feature request',
            'unable to access', 'cannot access', 'not working', 'stuck in',
            'auto-assign', 'auto-close', 'signed out', 'sign out',
            'not sending', 'not saved', 'not returning', 'not recorded'
        ]

        for phrase in exact_phrases:
            if phrase in text_lower:
                features['exact_phrases'].append(phrase)

        # Pattern matching with weights
        for category, data in self.patterns.items():
            for keyword in data['primary']:
                if keyword.lower() in text_lower:
                    features['primary_matches'].append((category, keyword, data['weight']))

            for keyword in data['secondary']:
                if keyword.lower() in text_lower:
                    features['secondary_matches'].append((category, keyword, data['weight'] * 0.5))

        return features

    def calculate_tag_scores(self, text: str, valid_tags: set) -> Dict[str, float]:
        """Calculate sophisticated scores for each valid tag"""
        features = self.extract_advanced_features(text)
        tag_scores = defaultdict(float)

        text_lower = text.lower()

        for tag in valid_tags:
            score = 0.0
            tag_normalized = tag.replace('_', ' ').lower()
            tag_words = tag.replace('_', ' ').split()

            # 1. EXACT TAG WORD MATCHING (highest priority)
            for tag_word in tag_words:
                if len(tag_word) > 2:
                    # Exact word boundary match
                    pattern = r'\b' + re.escape(tag_word) + r'\b'
                    matches = len(re.findall(pattern, text_lower))
                    if matches > 0:
                        score += 5.0 * matches  # Strong signal

            # 2. EXACT PHRASE MATCHING
            for phrase in features['exact_phrases']:
                phrase_words = phrase.split()
                overlap = set(phrase_words) & set(tag_words)
                if overlap:
                    score += 4.0 * len(overlap)

            # 3. PRIMARY PATTERN MATCHES
            for category, keyword, weight in features['primary_matches']:
                if category in tag or any(word in category for word in tag_words):
                    score += weight * 2.0
                # Check if keyword appears in tag
                if keyword in tag_normalized:
                    score += weight * 2.5

