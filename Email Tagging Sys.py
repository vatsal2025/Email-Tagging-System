class CustomerIsolatedTaggingSystem:
    """
    Manages customer-specific tag vocabularies and ensures isolation.
    Key principle: Each customer has their own tag vocabulary - no cross-contamination.
    """

    def __init__(self):
        self.customer_tags = defaultdict(set)  # customer_id -> set of valid tags
        self.customer_examples = defaultdict(list)  # customer_id -> list of examples

    def train(self, df: pd.DataFrame):
        """Build customer-specific tag vocabularies from training data"""
        print("🔧 Training isolation system...")
        for customer_id in df['customer_id'].unique():
            customer_data = df[df['customer_id'] == customer_id]

            # Store valid tags for this customer
            self.customer_tags[customer_id] = set(customer_data['tag'].unique())

            # Store examples for few-shot learning
            self.customer_examples[customer_id] = customer_data.to_dict('records')

        print(f"✅ Trained on {len(self.customer_tags)} customers")
        for cust_id, tags in self.customer_tags.items():
            print(f"   {cust_id}: {len(tags)} unique tags")

    def get_customer_context(self, customer_id: str, max_examples: int = 5) -> str:
        """Generate few-shot examples for a specific customer"""
        if customer_id not in self.customer_examples:
            return "No training data available for this customer."

        examples = self.customer_examples[customer_id][:max_examples]
        context = f"Valid tags for {customer_id}: {sorted(self.customer_tags[customer_id])}\n\n"
        context += "Example emails and their correct tags:\n"

        for i, ex in enumerate(examples, 1):
            context += f"\nExample {i}:\n"
            context += f"Subject: {ex['subject']}\n"
            context += f"Body: {ex['body'][:100]}...\n"
            context += f"Tag: {ex['tag']}\n"

        return context

    def validate_prediction(self, customer_id: str, predicted_tag: str) -> Tuple[bool, str]:
        """Validate that predicted tag belongs to customer's vocabulary"""
        if customer_id not in self.customer_tags:
            return False, f"Unknown customer: {customer_id}"

        if predicted_tag not in self.customer_tags[customer_id]:
            valid_tags = sorted(self.customer_tags[customer_id])
            return False, f"Invalid tag '{predicted_tag}' for {customer_id}. Valid: {valid_tags[:3]}..."

        return True, "Valid"

